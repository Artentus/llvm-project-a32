#ifndef LLVM_LIB_TARGET_A32_A32SUBTARGET_H
#define LLVM_LIB_TARGET_A32_A32SUBTARGET_H

#include "A32FrameLowering.h"
#include "A32ISelLowering.h"
#include "A32InstrInfo.h"
#include "llvm/CodeGen/SelectionDAGTargetInfo.h"
#include "llvm/CodeGen/TargetSubtargetInfo.h"
#include "llvm/IR/DataLayout.h"
#include "llvm/Target/TargetMachine.h"

#define GET_SUBTARGETINFO_HEADER
#include "A32GenSubtargetInfo.inc"

namespace llvm {
class StringRef;

class A32Subtarget : public A32GenSubtargetInfo {
  virtual void anchor();
  A32FrameLowering FrameLowering;
  A32InstrInfo InstrInfo;
  A32RegisterInfo RegInfo;
  A32TargetLowering TLInfo;
  SelectionDAGTargetInfo TSInfo;

  /// Initializes using the passed in CPU and feature strings so that we can
  /// use initializer lists for subtarget initialization.
  A32Subtarget &initializeSubtargetDependencies(StringRef CPU, StringRef TuneCPU, StringRef FS);

public:
  // Initializes the data members to match that of the specified triple.
  A32Subtarget(const Triple &TT, StringRef CPU, StringRef TuneCPU,
                 StringRef FS, const TargetMachine &TM);

  // Parses features string setting specified subtarget options. The
  // definition of this function is auto-generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef TuneCPU, StringRef FS);

  const A32FrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }
  const A32InstrInfo *getInstrInfo() const override { return &InstrInfo; }
  const A32RegisterInfo *getRegisterInfo() const override {
    return &RegInfo;
  }
  const A32TargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }
  const SelectionDAGTargetInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }
};
} // End llvm namespace

#endif
